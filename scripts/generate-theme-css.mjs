#!/usr/bin/env node
/**
 * generate-theme-css.mjs
 *
 * Reads config/theme.yml (global tokens) and hackathons/<slug>/theme.yml (per-hackathon overrides)
 * then generates packages/ui/src/styles/generated-themes.css with :root, .dark, and
 * [data-hackathon="<slug>"] CSS blocks.
 *
 * Run: node scripts/generate-theme-css.mjs
 */
import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

const GLOBAL_THEME_PATH = path.join(ROOT, 'config/theme.yml');
const HACKATHONS_DIR = path.join(ROOT, 'hackathons');
const OUT_FILE = path.join(ROOT, 'packages/ui/src/styles/generated-themes.css');

/**
 * Render a set of CSS custom properties as indented declarations.
 */
function renderTokens(tokens, indent = '  ') {
  return Object.entries(tokens)
    .map(([key, value]) => `${indent}--${key}: ${value};`)
    .join('\n');
}

/**
 * Read and parse a YAML file, returning null if it doesn't exist.
 */
function readYamlFile(filePath) {
  if (!existsSync(filePath)) return null;
  const content = readFileSync(filePath, 'utf-8');
  return yaml.load(content);
}

function main() {
  console.log('[generate-theme-css] Reading theme config...');

  const globalTheme = readYamlFile(GLOBAL_THEME_PATH);
  if (!globalTheme) {
    console.error(`[generate-theme-css] ERROR: ${GLOBAL_THEME_PATH} not found`);
    process.exit(1);
  }

  const lines = [];
  lines.push('/* AUTO-GENERATED — DO NOT EDIT */');
  lines.push('/* Generated by scripts/generate-theme-css.mjs from config/theme.yml */');
  lines.push('');

  // --- :root block ---
  lines.push(':root {');
  if (globalTheme.radius) {
    lines.push(`  --radius: ${globalTheme.radius};`);
  }
  if (globalTheme.light) {
    lines.push('');
    lines.push(renderTokens(globalTheme.light));
  }
  lines.push('}');
  lines.push('');

  // --- .dark block ---
  if (globalTheme.dark) {
    lines.push('.dark {');
    lines.push(renderTokens(globalTheme.dark));
    lines.push('}');
    lines.push('');
  }

  // --- Per-hackathon overrides ---
  const hackathonSlugs = [];
  if (existsSync(HACKATHONS_DIR)) {
    const entries = readdirSync(HACKATHONS_DIR, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      const themePath = path.join(HACKATHONS_DIR, entry.name, 'theme.yml');
      if (existsSync(themePath)) {
        hackathonSlugs.push(entry.name);
      }
    }
  }

  // Sort alphabetically for deterministic output
  hackathonSlugs.sort();

  for (const slug of hackathonSlugs) {
    const themePath = path.join(HACKATHONS_DIR, slug, 'theme.yml');
    const hackathonTheme = readYamlFile(themePath);
    if (!hackathonTheme) continue;

    if (hackathonTheme.light) {
      lines.push(`[data-hackathon="${slug}"] {`);
      lines.push(renderTokens(hackathonTheme.light));
      lines.push('}');
    }
    if (hackathonTheme.dark) {
      lines.push(`.dark [data-hackathon="${slug}"] {`);
      lines.push(renderTokens(hackathonTheme.dark));
      lines.push('}');
    }
    lines.push('');
  }

  // Ensure output directory exists
  const outDir = path.dirname(OUT_FILE);
  if (!existsSync(outDir)) {
    mkdirSync(outDir, { recursive: true });
  }

  const output = lines.join('\n');
  writeFileSync(OUT_FILE, output);

  console.log(`[generate-theme-css] Written to ${path.relative(process.cwd(), OUT_FILE)}`);
  console.log(`  hackathon overrides: ${hackathonSlugs.length}`);
}

main();
